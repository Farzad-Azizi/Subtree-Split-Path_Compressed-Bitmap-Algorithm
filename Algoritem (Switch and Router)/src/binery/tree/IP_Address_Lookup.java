    private void SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchActionPerformed
        try {
            String ip = "";
            int Oct_1 = Integer.parseInt(O_1.getText());
            int Oct_2 = Integer.parseInt(O_2.getText());
            int Oct_3 = Integer.parseInt(O_3.getText());
            int Oct_4 = Integer.parseInt(O_4.getText());
            int S_mac = Integer.parseInt(Mac.getText());

            b.bin(Oct_1);
            String div1 = b.g();
            b.Clear();
            ip += String.format("%8s", div1).replace(' ', '0');
            b.bin(Oct_2);
            String div2 = b.g();
            b.Clear();
            ip += String.format("%8s", div2).replace(' ', '0');
            b.bin(Oct_3);
            String div3 = b.g();
            b.Clear();
            ip += String.format("%8s", div3).replace(' ', '0');
            b.bin(Oct_4);
            String div4 = b.g();
            b.Clear();
            ip += String.format("%8s", div4).replace(' ', '0');
            String ip1 = ip;
            ip = ip.substring(0, S_mac);

            if (ComboBox1.getSelectedItem().equals("Binary Trie")) {
                String re = bt.search_binery_tree(ip);
                System.out.println("Node found Algoritem:" + re);
                System.out.println("\n--------------------------------End Algoritem_Binery_tree--------------------------------");

                JOptionPane.showMessageDialog(null, "Search result from the algoritem Binery Trie:\n'" + "\t" + re + "'\n*implementation details (Binery_tree) in the output comment*");
            }

            if (ComboBox1.getSelectedItem().equals("Subtree-Split")) {
                bt.sub_te(ip);
                JOptionPane.showMessageDialog(null, "Search result from the algoritem & *implementation details (Subtree-Split) in the output comment*");
            }

            if (ComboBox1.getSelectedItem().equals("Level Compression Trie")) {
                String g = bt.le_te(ip);
                System.out.println("Node found Algoritem:" + g);
                JOptionPane.showMessageDialog(null, "Search result from the algoritem Level Compression Trie:\n'" + "\t" + g + "'\n*implementation details (Level Compression Trie) in the output comment*");
            }
            if (ComboBox1.getSelectedItem().equals("Bitmap tree ")) {
                String g = bt.Bit_map(ip1,tree);
               // TreeBitmap t = tree.getTreeBitmap();
                JOptionPane.showMessageDialog(null, "Search result from the algoritem Bitmap tree:\n'" + "\t" + g);

            }
        } catch (HeadlessException | NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "مشکل در ورودی اعداد وجود دارد", "", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_SearchActionPerformed

