    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        if (flag == 1) {
            String[] s = null;
            String ip = "";
            try {
                JFileChooser jfc = new JFileChooser(FileSystemView.getFileSystemView().getHomeDirectory());
                FileFilter text = new FileNameExtensionFilter("Text file", "txt");
                jfc.setFileFilter(text);
                jfc.setAcceptAllFileFilterUsed(false);
                jfc.setDialogTitle("Open a file(.txt)");
                int returnValue = jfc.showOpenDialog(null);

                if (returnValue == JFileChooser.APPROVE_OPTION) {
                    File selectedFile = jfc.getSelectedFile();
                    File file = new File(selectedFile.getAbsolutePath());
                    if (file.exists()) {
                        flag = 0;
                        BufferedReader bu = new BufferedReader(new FileReader(file));
                        String line;
                        while ((line = bu.readLine()) != null) {
                            s = null;
                            s = line.split("/", 6);
                            b.bin(Integer.parseInt(s[0]));
                            ip += String.format("%8s", b.g()).replace(' ', '0');
                            b.Clear();
                            b.bin(Integer.parseInt(s[1]));
                            ip += String.format("%8s", b.g()).replace(' ', '0');
                            b.Clear();
                            b.bin(Integer.parseInt(s[2]));
                            ip += String.format("%8s", b.g()).replace(' ', '0');
                            b.Clear();
                            b.bin(Integer.parseInt(s[3]));
                            ip += String.format("%8s", b.g()).replace(' ', '0');
                            b.Clear();
                            int sub = Integer.parseInt(s[4]);
                            String ip1 = ip;
                            ip = ip.substring(0, sub);

                            bt.insert(ip, s[5]);
                            // Add bitmap
                            tree.insertIP(ip1, sub, s[5]);
                            //
                            ip = "";
                            ip1 = "";
                            DefaultTableModel model = (DefaultTableModel) table_ip.getModel();
                            model.addRow(new Object[]{s[0] + "." + s[1] + "." + s[2] + "." + s[3], s[4], s[5]});
                            ip = "";
                        }
                    } else {
                        System.out.println(file.getAbsoluteFile() + "\n  not exist");
                    }
                }

            } catch (FileNotFoundException e) {
            } catch (IOException ex) {
                Logger.getLogger(IP_Address_Lookup.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else {
            JOptionPane.showMessageDialog(null, "ای پی ها قبلا ذخیره شدند.", "", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_addButtonActionPerformed

